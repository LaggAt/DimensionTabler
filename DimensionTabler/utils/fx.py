

def first(iter):
    if len(iter):
        return iter[0]
    else:
        return None

def last(iter):
    if len(iter):
        return iter[-1]
    else:
        return None

def min(iter):
    if len(iter):
        return min(iter)
    else:
        return None

def max(iter):
    if len(iter):
        return max(iter)
    else:
        return None

